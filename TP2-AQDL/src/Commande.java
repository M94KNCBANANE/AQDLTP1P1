public class Commande {

	public String Nom;
	public String Plat;
	public int Quantite;
	
	public String getNom() {
		
		return Nom;
	}
	public void setNom( String nom ) {
		
		Nom = nom;
	}
	
	public String getPlat() {
		
		return Plat;
	}
	
	public void setPlat( String plat ) {
		
		Plat = plat;
	}
	
	public int getQuantite() {
		
		return Quantite;
	}
	
	public void setQuantite( int quantite ) {
		
		Quantite = quantite;
	}
	
}
